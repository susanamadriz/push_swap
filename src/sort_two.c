#include "push_swap.h"

void	sort_two(t_stack **a)
{
	if ((*a)->value > (*a)->next->value)
	{
		// swap
		int temp = (*a)->value;
		(*a)->value = (*a)->next->value;
		(*a)->next->value = temp;
		write(1, "sa\n", 3);
	}
}
